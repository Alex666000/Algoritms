// Дан массив агр [1, 8, 3, 9, 12, 13, 2, 5, 6], необходимо отсортировать четные числа по возрастанию и оставить их на мест

/* Фраза "оставить числа на месте" означает, что четные числа должны быть отсортированы только между собой,
но их позиции в массиве не должны измениться относительно нечетных чисел. То есть нечетные числа должны остаться на тех же местах,
где они были, а только четные числа сортируются.
 */

// Функция для сортировки четных чисел в массиве, сохраняя исходное расположение всех чисел
const sortNums = (array) => {
  // Создаем массив для хранения четных чисел
  const evens = [];

  // Проходим по каждому элементу массива
  array.forEach(num => {
    // Если число четное, добавляем его в массив `evens`
    if (num % 2 === 0) {
      // push копирует элементы из array и добавляет в evens  мутируя его (сначала он пуст)
      evens.push(num);
    }
  });

  // Сортируем массив четных чисел по возрастанию
  evens.sort((a, b) => a - b);

  // Создаем новый массив, в котором заменяем исходные несортированный четные числа на отсортированные [1, 2, 3, 9, 6, 13, 8, 5, 12]
  return array.map((num) => {
    // Если элемент четный, заменяем его на первое значение из `evens`
    if (num % 2 === 0) {
      // `shift()` извлекает первое значение из `evens` и возвращает его, чтобы поставить на место текущего четного числа
      return evens.shift()
    }
    // Если элемент нечетный, оставляем его без изменений
    return num;
  });
};

// Пример использования функции
const array = [1, 8, 3, 9, 12, 13, 2, 5, 6];
console.log(sortNums(array)); // [1, 2, 3, 9, 6, 13, 8, 5, 12]


