// '—Ç–µ–≤–∏—Ä–ü '–∫–∞–∫' '–∞–ª–µ–î'

const reverseStr = (str) => {
  return str.split(' ').reduce((acc, word) => {
    const revertedWord = word.split('').reverse().join('')
    acc.push(revertedWord)
    return acc
  }, []).join(' ')
}

